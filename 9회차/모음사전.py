def solution(word):
    a = [[5,   6,    7,    8,    9],
         [4,  10,   16,   22,   28],
         [3,  34,   65,   96,  127],
         [2, 158,  314,  470,  626],
         [1, 782, 1563, 2344, 3125],
         [0,   0,    0,    0,    0]]
    b, c = 4, 0
    for i in word:
        if(i == "A"): i = 0
        elif(i == "E"): i = 1
        elif(i == "I"): i = 2
        elif(i == "O"): i = 3
        elif(i == "U"): i = 4
        c += (a[b][i] - a[b + 1][0])
        b -= 1
    return c
